const mongoose = require("mongoose");

const LogInSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    number: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});
const loginData = mongoose.model('user',LogInSchema);

module.exports = loginData;
